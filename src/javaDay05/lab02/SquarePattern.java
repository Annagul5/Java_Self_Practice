package javaDay05.lab02;

public class SquarePattern {
    public static void main(String[] args) {
        int giveNum=6;
        for (int i = 1; i <=giveNum ; i++) {
            for (int j = 1; j <=giveNum ; j++) {
                System.out.print("* ");

            }
            System.out.println();
        }
    }
}
